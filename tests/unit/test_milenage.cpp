#include "crypt/milenage.hpp"

#include <gtest/gtest.h>

using namespace ims::crypt;

TEST(MilenageTest, ComputesKnownOpc) {
    const std::array<uint8_t, 16> k = {
        0x46, 0x5b, 0x5c, 0xe8, 0xb1, 0x99, 0xb4, 0x9f,
        0xaa, 0x5f, 0x0a, 0x2e, 0xe2, 0x38, 0xa6, 0xbc,
    };
    const std::array<uint8_t, 16> op = {
        0xcd, 0xc2, 0x02, 0xd5, 0x12, 0x3e, 0x20, 0xf6,
        0x2b, 0x6d, 0x67, 0x6a, 0xc7, 0x2c, 0xb3, 0x18,
    };
    const std::array<uint8_t, 16> expected_opc = {
        0xcd, 0x63, 0xcb, 0x71, 0x95, 0x4a, 0x9f, 0x4e,
        0x48, 0xa5, 0x99, 0x4e, 0x37, 0xa0, 0x2b, 0xaf,
    };

    auto opc = compute_opc(k, op);
    ASSERT_TRUE(opc.has_value()) << opc.error().message;
    EXPECT_EQ(*opc, expected_opc);
}

TEST(MilenageTest, GeneratesKnownReferenceVector) {
    const std::array<uint8_t, 16> k = {
        0x46, 0x5b, 0x5c, 0xe8, 0xb1, 0x99, 0xb4, 0x9f,
        0xaa, 0x5f, 0x0a, 0x2e, 0xe2, 0x38, 0xa6, 0xbc,
    };
    const std::array<uint8_t, 16> opc = {
        0xcd, 0x63, 0xcb, 0x71, 0x95, 0x4a, 0x9f, 0x4e,
        0x48, 0xa5, 0x99, 0x4e, 0x37, 0xa0, 0x2b, 0xaf,
    };
    const std::array<uint8_t, 6> sqn = {0xff, 0x9b, 0xb4, 0xd0, 0xb6, 0x07};
    const std::array<uint8_t, 2> amf = {0xb9, 0xb9};
    const std::array<uint8_t, 16> rand = {
        0x23, 0x55, 0x3c, 0xbe, 0x96, 0x37, 0xa8, 0x9d,
        0x21, 0x8a, 0xe6, 0x4d, 0xae, 0x47, 0xbf, 0x35,
    };

    auto vector = generate_vector(opc, k, sqn, amf, rand);
    ASSERT_TRUE(vector.has_value()) << vector.error().message;

    const std::array<uint8_t, 8> expected_xres = {
        0xa5, 0x42, 0x11, 0xd5, 0xe3, 0xba, 0x50, 0xbf,
    };
    const std::array<uint8_t, 16> expected_ck = {
        0xb4, 0x0b, 0xa9, 0xa3, 0xc5, 0x8b, 0x2a, 0x05,
        0xbb, 0xf0, 0xd9, 0x87, 0xb2, 0x1b, 0xf8, 0xcb,
    };
    const std::array<uint8_t, 16> expected_ik = {
        0xf7, 0x69, 0xbc, 0xd7, 0x51, 0x04, 0x46, 0x04,
        0x12, 0x76, 0x72, 0x71, 0x1c, 0x6d, 0x34, 0x41,
    };
    const std::array<uint8_t, 6> expected_ak = {
        0xaa, 0x68, 0x9c, 0x64, 0x83, 0x70,
    };
    const std::array<uint8_t, 16> expected_autn = {
        0x55, 0xf3, 0x28, 0xb4, 0x35, 0x77, 0xb9, 0xb9,
        0x4a, 0x9f, 0xfa, 0xc3, 0x54, 0xdf, 0xaf, 0xb3,
    };

    EXPECT_EQ(vector->xres, expected_xres);
    EXPECT_EQ(vector->ck, expected_ck);
    EXPECT_EQ(vector->ik, expected_ik);
    EXPECT_EQ(vector->ak, expected_ak);
    EXPECT_EQ(vector->autn, expected_autn);
}
